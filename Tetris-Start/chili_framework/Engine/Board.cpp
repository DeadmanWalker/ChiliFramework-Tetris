#include "Board.h"
#include <assert.h>

Board::Board(Graphics& gfx)
	:
	gfx(gfx)
{
}

void Board::DrawCell(const Location& loc, Color c) const
{
	assert(loc.x >= 0);
	assert(loc.y >= 0);
	assert(loc.x < width);
	assert(loc.y < height);

	if (loc.y >= nUndisplay_row)
	{
		int off_x = x + borderPadding + borderWidth + cellPadding;
		int off_y = y - nUndisplay_row * dimension + borderPadding + borderWidth + cellPadding;
		gfx.DrawRectDim(off_x + loc.x * dimension, off_y + loc.y * dimension, dimension - 2 * cellPadding, dimension - 2 * cellPadding, c);
	}
}

void Board::DrawBorder() const
{
	int top = y;
	int left = x;
	int bottom = top + ndisplay_row * dimension + 2 * (borderPadding + borderWidth);
	int right = left + width * dimension + 2 * (borderPadding + borderWidth);

	gfx.DrawRect(left, top, right, top + borderWidth, borderColor);
	gfx.DrawRect(left, bottom - borderWidth, right, bottom, borderColor);
	gfx.DrawRect(left, top + borderWidth, left + borderWidth, bottom - borderWidth, borderColor);
	gfx.DrawRect(right - borderWidth, top + borderWidth, right, bottom - borderWidth, borderColor);
}

void Board::DrawBackground() const
{
	gfx.DrawRectDim(x + borderWidth, y + borderWidth, width * dimension + 2 * borderPadding, ndisplay_row * dimension + 2 * borderPadding, backgroundLineColor);
	for (int i = 0; i < width; ++i)
	{
		for (int j = 3; j < height; ++j)
		{
			DrawCell({ i,j }, backgroundColor);
		}
	}
}

void Board::DrawTile() const
{
	for (int i = 0; i < width; ++i)
	{
		for (int j = nUndisplay_row; j < height; ++j)
		{
			if (tiles[i][j].isOccupy)
			{
				DrawCell({ i,j }, tiles[i][j].tileColor);
			}
		}
	}
}

void Board::MoveRows(int row)
{
	for (int i = 0; i < width; ++i)
	{
		for (int j = row; j > 0; --j)
		{
			tiles[i][j].tileColor = tiles[i][j - 1].tileColor;
			tiles[i][j].isOccupy = tiles[i][j - 1].isOccupy;
		}
	}
}

void Board::ClearRow()
{
	line_combo = 0;
	for (int j = 0; j < height; ++j)
	{
		bool isLineFill = true;
		for (int i = 0; i < width; ++i)
		{
			if(!tiles[i][j].isOccupy)
			{
				isLineFill = false;
				break;
			}
		}
		if (isLineFill)
		{
			MoveRows(j);
			++line_combo;
		}
	}
}

void Board::SetTile(const Location& loc, Color c)
{
	tiles[loc.x][loc.y].isOccupy = true;
	tiles[loc.x][loc.y].tileColor = c;
}

int Board::GetGridHeight() const
{
	return height;
}

int Board::GetGridWidth() const
{
	return width;
}

bool Board::IsTileOccupy(Location loc) const
{
	return tiles[loc.x][loc.y].isOccupy;
}

