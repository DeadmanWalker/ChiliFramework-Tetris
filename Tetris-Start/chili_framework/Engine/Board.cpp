void Board::DrawGhostTile(Location loc, Color c) const
{
	int top = y + (loc.y - nUndisplay_row )* dimension + borderPadding + borderWidth;
	int right = x + loc.x * dimension + borderPadding + borderWidth;
	int bottom = top + dimension;
	int left = right + dimension;

	gfx.DrawRectDim(right, top, dimension, cellPadding, c);
	gfx.DrawRectDim(right, top + cellPadding, cellPadding, dimension - cellPadding, c);
	gfx.DrawRectDim(right, bottom - cellPadding, dimension, cellPadding, c);
	gfx.DrawRectDim(left - cellPadding, top + cellPadding, cellPadding, dimension - cellPadding, c);
}

